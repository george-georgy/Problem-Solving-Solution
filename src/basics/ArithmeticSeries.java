package basics;

public class ArithmeticSeries {

    // program that prints the terms of the arithmetic sequence 1 , 6 , 11 , . . . < 100 1,6,11,...<100.
    public static void main(String[] args) {
        for (int i = 1; i < 100 ; i = i + 5){
            System.out.println(i);
        }
    }

}
